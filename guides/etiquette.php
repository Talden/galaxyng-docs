			<p>by Chris Orr</p>
			
			<p>This is my short list of things to do and not to do in Galaxy. I try to play this way, and hopefully others do also. If you have a suggestion or comment send it on.</p>
			
			<p>Chrisfucius say:</p>
			
			<h2 id="alliances">Alliances</h2>
				<p id="i1">1. Don't ever break an alliance. It is acceptable to fight a former ally if they broke the alliance. Remember that people remember email addresses and race names, and if you get a reputation as an alliance breaker, you might run out of people to ally with.</p>
				<p id="i2">2. Do be explicit in terms of alliances, especially temporary ones. Always set time limits in turns, give specific planets as boundaries, explicitly allow scouting or not, define terms that can break the alliance, be as verbose as possible.</p>
				<p id="i3">3. Do differentiate between end-game and temporary alliances. Don't make end-game alliances lightly, make sure your ally can keep up with you and won't be a liability. Most alliances should start as temporary that are later "upgraded" to end-game. There is nothing worse than discovering that the race you made an alliance with in turn 3 doesn't know what flak is for.</p>
				<p id="i4">4. Do seal an end-game alliance with exchanges of turn reports. This prevents surprises and allows for much better communication, such as "Hey send group 232 to HW121 next turn." Obviously this doesn't apply to highlander games.</p>
				
			<hr />
			
			<h2 id="communication">Communication</h2>
				<p id="i5">5. Do send messages in character, don't lie out of character. I always bracket my "in character" messages with "START MESSAGE...END MESSAGE" so people will know it is not ME that is blustering, threatening, and spreading disinformation. I am most definitely not like that in real life, so I like people to know when it is me and when it is Price Throatcrusher of the 
Bloodletters.</p>
				<p id="i6">6. Do spread disinformation, but be careful. This can be useful with enemies, but don't do it much with allies, and definitely not with end-game allies. Do it too much and no one will believe anything you say anyhow. (see <a href="#i5">#5</a>)</p>
				<p id="i7">7. Do use the mailing lists to banter. It keeps the game lively and interesting. Publish "newsletters" that goad and belittle your opponents, or over-emphasize your victories. This is a good place for a bit of disinformation. (See <a href="#i6">#6</a> and <a href="#i9">#9</a>)</p>

			<hr />

			<h2 id="war">War</h2>
				<p id="i8">8. Do attack non-allies without provocation. Crush 'em, break their production backbone before they even see you coming.</p>
				<p id="i9">9. Don't gloat over victories (much). Be careful here, people may come after you in later games because of gloating. Just stomp your enemies and be done with it.</p>
				<p id="i10">10. Do fight to the end. Don't give up because someone bombed your 1000 homeworld, I have had some of mine bombed and come back to win, and the other way around as well. Most of the time the attacker is spread pretty thin in order to hit you hard, so an immediate counter strike will usually do some damage. If things get bad enough or you have to quit, issue the Q command so the attacker doesn't get a free lunch (in NG).</p>
				
			<hr />
			
			<h2 id="general">General</h2>
				<p id="i11">11. Don't sign up for games you have no intention of playing. Personally I think we should have a black list of players who do this and not let them sign up again for six months or so. This is not only rude it unbalances the game. I would rather be surrounded by non-players than up against a border, since in NG you can get tons of resources from bombing your leaderless neighbors.</p>
				<p id="i12">12. Don't advertise your lack of clue. If you are new to the game, don't send messages to the game list that show you are vulnerable to attack. This includes messages like gone from the game for a couple turns, don't know how to do something, etc. If you must ask, generalize your question and post it to the <a href="http://lists.sourceforge.net/lists/listinfo/galaxyng-players">general discussion mailing list</a> FROM ANOTHER EMAIL ADDRESS. I troll all the lists when I am in a big game and look for victims.</p>

