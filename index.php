			<h2>Introduction</h2>
				<p>Galaxy is a free play by email interstellar wargame for multiple players.  The object of the game is to conquer the galaxy by colonizing and developing uninhabited planets and attacking neighboring races.  GalaxyNG is a <a href="variants.php">variant</a> of Galaxy created by <a href="http://sourceforge.net/users/gumpu/">Frans Slothouber</a> which introduced multiple homeworlds and partial bombings.  It is the variant that is most similar to original Galaxy.</p>
				<p>GalaxyNG games usually have between 10 and 50 players, run 1 to 5 turns per week and last between 2 and 24 months.  Players email orders to a GalaxyNG server and receive text or XML forecasts and turn reports from the server by email.  Most players use a <a href="http://galaxyview.sourceforge.net/tools.html">turn viewer</a> to display the map and create orders.  Players can design and build ships, colonize and develop planets, research technology and fight battles to conquer the galaxy.  Winners are listed in the <a href="http://www.refs-home.net/galaxyng/hof/">Hall of Fame</a>.</p>
				<p>New games are announced on the <a href="http://lists.sourceforge.net/lists/listinfo/galaxyng-players">galaxyng-players</a> mailing list.  They can also be found on GalaxyNG server web sites:</p>
